package th.ac.nu.students.u54347491.compro.ch02;


public class FloatDouble {

    
    public static void main(String[] args) {
       
        float float_pi = 3.145926535897896F;
        //หากต้องการระบุขนาดเป็น 32 บิต ต้องต่อท้าย liteal ด้วย F
        double double_pi = 3.145926535897896D;
        //หากต้องการระบุขนาดเป็น 64 บิต ต้องต่อท้าย liteal ด้วย D
        System.out.println(float_pi); // 3.1459265
        System.out.println(double_pi); // 3.145926535897896
    }
}
